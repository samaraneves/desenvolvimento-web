const a = 7
let b = 3
    //O = está como papel de atribuição. variavelAleatoria recebe o valor 7.
    // Chave variavelAleatoria, valor 7.
b += a // b = b + a resultado: 10 - Atribuição somativa.
console.log(b) ///10
    /*Atribuição somativa, consiste em atribuir o valor para a variável do valor dele mesmo mais outro.*/
b -= 3 //b=b-3
console.log(b) //7
    /*Atribuição subtrativa, consiste em atribuir o valor para a variável do valor dele mesmo menos outro.*/
b *= 2 //b=b*2
console.log(b) //14
    /*Atribuição multiplicativa, consiste em atribuir o valor para a variável do valor dele mesmo vezes outro.*/
b /= 2 //b=b/2
console.log(b) //7
    /*Atribuição divisiva, consiste em atribuir o valor para a variável do valor dele mesmo dividido o do outro.*/
b %= 2 //b=b%2 seria o resto da divisão de b/2,esse é o módulo.
console.log(b)
    /*Atribuição modular, consiste em atribuir o valor para a variável do valor dele mesmo pelo módulo da div
    divisão do outro.*/